a = "hello";

print(a[2]);
print(a[-3]);

print('================================================================================================')

# slice
# it allows us to make a sub-sting
c = a[0:]
print(c);
d = a[0:3];
print(d);

print('================================================================================================')

# syntax : [start_index:stop_index:(steps=hops)]
b = a[0:3:2];
print(b);

print('================================================================================================')

#step size (jumps)
e = a[::2]
print(e);

print('================================================================================================')

#we can reverse the string by this method
print(a[::-1]);
