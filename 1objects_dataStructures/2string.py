print("\n================================================================================================\n")

a = "hello"

print(a[2])
print(a[-3])
print(a[-1])

print("\n================================================================================================\n")

# slice
# it allows us to make a sub-sting
c = a[0:]
print(c)
d = a[0:3]
print(d)

print("\n================================================================================================\n")

# syntax : [start_index:stop_index:(steps=hops)]
b = a[0:3:2]
print(b)

print("\n================================================================================================\n")

# step size (jumps)
e = a[::2]
print(e)

print("\n================================================================================================\n")

# we can reverse the string by this method
print(a[::-1])


