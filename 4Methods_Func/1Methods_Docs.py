# discussing methods

# append() method will add a new element
print("Append method")
mylist = [1, 2, 3, 4]
mylist.append(5)
print(mylist)

print("\n================================================================================================\n")

# pop() method will remove an element
print("pop method")
mylist.pop()
print(mylist)

print("\n================================================================================================\n")

# to see all the methods available to us write the variable_name. and press 'space' + 'ctrl'

# insert() method will insert an element in a specified index no. like variable_name.insert(2,5) now it will insert 5 in index no. 2
print("insert method")
mylist.insert(2, 5)
print(mylist)

print("\n================================================================================================\n")

# help function defines a specific a function or a method
help(mylist.insert)

print("\n================================================================================================\n")
