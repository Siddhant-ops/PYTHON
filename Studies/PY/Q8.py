# Write a python program that accepts value from user and performs arithmetic operations on it.

in_num_x = float(input("Enter a value for 'x' : "))
in_num_y = float(input("Enter a value for 'y' : "))

print("\n==================================================\n")

print(f"Addition : {in_num_x + in_num_y}")

print("\n==================================================\n")

print(f"Division : {in_num_x / in_num_y}")

print("\n==================================================\n")

print(f"Subtraction : {in_num_x - in_num_y}")

print("\n==================================================\n")

print(f"Multiplication : {in_num_x * in_num_y}")

print("\n==================================================\n")

print(f"Floor division : {in_num_x // in_num_y}")

print("\n==================================================\n")

print(f"Exponential : {in_num_x ** in_num_y}")

print("\n==================================================\n")

print(f"Modulus : {in_num_x % in_num_y}")
